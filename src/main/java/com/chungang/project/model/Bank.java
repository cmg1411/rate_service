package com.chungang.project.model;

public class Bank {

	private double bankno;
	private String bankna;
	private double fees;

	public double getBankNo() {
		return bankno;
	}

	public void setBankNo(double bankNo) {
		bankno = bankNo;
	}

	public String getBankNa() {
		return bankna;
	}

	public void setBankNa(String bankNa) {
		bankna = bankNa;
	}

	public double getFees() {
		return fees;
	}

	public void setFees(double fees) {
		this.fees = fees;
	}

}
